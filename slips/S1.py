# Write a Python program to create a list and update the second element of the
# list.


# Create a list with some initial elements
my_list = [10, 20, 30, 40, 50]

# Display the original list
print("Original list:", my_list)

# Update the second element (index 1) of the list
my_list[1] = 99  # Changing the second element to 99

# Display the updated list
print("Updated list:", my_list)